package chapter04;

public class AssignTest_01 {

	public static void main(String[] args) {
		//더하기 빼기 곱하기 나누기 나머지
		int avar=20;
		System.out.println("avar +=10: "+(avar+= 10));
		//aVar=aVar+10;
		System.out.println("avar -=10: "+(avar-= 10));
		//aVar=avar-10;
		System.out.println("avar *=10: "+(avar*= 10));
		//aVar=avar*10;
		System.out.println("avar /=10: "+(avar/=10));
		//aVar=avar/10;
		System.out.println("avar %=10: "+(avar%= 10));
		//aVar=avar%10;
		
	}

}
