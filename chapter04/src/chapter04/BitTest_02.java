package chapter04;

public class BitTest_02 {

	public static void main(String[] args) {
		
		int a,b,c;
		a=30;
		b=25;
		
		//&
		  c=a & b;
		System.out.println(c);
		
		//|
		  c=a | b;
		System.out.println(c);
		//^
		  c=a ^ b;
		System.out.println(c);
		
		//~
		c=~(-1);
		System.out.println(c);
	}

}
